function complement() {
    const info = document.getElementById("complement");
    const btninfo = document.getElementById("button-complement");

    if (info.style.display == "none") {
        info.style.display = "inline";
        btninfo.innerHTML = "<";
    } else {
        info.style.display = "none";
        btninfo.innerHTML = ">";
    }
}
